import math

radio = float(input("Entra el radio del circulo: "))

area = math.pi * radio**2
longitud = 2 * math.pi * radio

print(f"El area es igual a: {area:.2f}") # Redondeamos el resultado a 2 decimales
print(f"La longitud es igual a: {longitud:.2f}") # Redondeamos el resultado a 2 decimales