# Funciones integradas

# Pasar un string a float
n = float("10.8")
print(n)

# Pasar un int a string
n = str(10)
print(n)

# Pasar un valor int a binario. Prefijo = 0b
n = bin(10)
print(n)

# Pasar un valor int a hexadecimal. Prefijo = Ox
n = hex(10)
print(n)

# Pasar un valor binario a int
n = int("0b1001",2) # int("0b****",2)
print(n)

# Pasar un hexadecimal a int
n = int("0xa",16) # int(0x****,16)
print(n)

# Valor absoluto
n = abs(-8)
print(n)

# Redondear un numero
n = round(8.8)
print(n)

# Logintud de una cadena
n = len("Marc Villalobos")
print(n)