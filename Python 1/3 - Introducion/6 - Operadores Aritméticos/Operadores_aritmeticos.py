# Operadores aritmeticos

num1 = 10
num2 = 6

resultado = num1 + num2
resultado2 = num1 - num2
resultado3 = num1 * num2
resultado4 = num1 / num2
resultado5 = num1 // num2 # División redondeada a la baja
resultado6 = num1 % num2 # Residuo
resultado7 = num1 ** num2 # Potencia = 10 elevado a 6

print("Suma: ",resultado)
print("Resta: ",resultado2)
print("Multiplicación: ",resultado3)
print("División: ",resultado4)
print("División redondeada: ",resultado5)
print("Residuo de la división: ",resultado6)
print("Potencia: ",resultado7)